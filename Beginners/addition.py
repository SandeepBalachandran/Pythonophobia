# Python3 program to add two numbers
# Generate a random number between 0 and 100

# importing the random module
import random
num1 = random.randint(0,100)
num2 = random.randint(0,100)
  
# Adding two nos 
sum = num1 + num2 
  
# printing values 
print("Sum of {0} and {1} is {2}" .format(num1, num2, sum));

